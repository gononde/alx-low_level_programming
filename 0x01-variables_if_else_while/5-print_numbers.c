#include<stdio.h>
/**
 * main - print 10 digit from 0
 *
 * Description : using main function
 * this function is "prints all single digit numbers of base 10 starting from 0
 * Return: 0 after printing
 */
int main(void)
{
	int i;

	for (i = 0 ; i < 10; i++)
	
	{
		printf("%i", i);

	}

	printf('\n');

	return (0);

}
